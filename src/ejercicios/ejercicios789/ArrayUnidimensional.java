package ejercicios.ejercicios789;

public class ArrayUnidimensional {
    public static void main(String[] args) {
        String[] arrayUnidimensional = {
                "Uno",
                "Dos",
                "Tres",
                "Cuatro",
                "Cinco"
        };
        for (String elemento : arrayUnidimensional) {
            System.out.println(elemento);
        }
    }
}
